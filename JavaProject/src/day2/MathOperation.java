package day2;

public class MathOperation {
	
	public static float PI = 3.14f;//Since PI is 'public' and 'static' it can be accessed from anywhere using ClassName.variableName format
	
	public static int doAdd(int x, int y) {
		return x + y;
	}
	public static int doSubtract(int x, int y) {
		return x - y;
	}
}
